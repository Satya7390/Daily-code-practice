
public class Swap {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        int temp = a;
        a = b;
        b = temp;
        temp = a;
        System.out.println("Now the Value of a is : " + a);
        System.out.println("Now the Value of b is : " + b);

    }

}
